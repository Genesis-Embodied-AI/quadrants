def _get_matrix_swizzle_apis():
    swizzle_gen = qd.lang.matrix._generate_swizzle_patterns
    KEMAP_SET = ["xyzw", "rgba", "stpq"]
    res = []
    for key_group in KEMAP_SET:
        sw_patterns = swizzle_gen(key_group, required_length=4)
        res += sw_patterns
    return sorted(res)


